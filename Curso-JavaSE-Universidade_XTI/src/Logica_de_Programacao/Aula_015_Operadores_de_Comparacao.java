public class Aula_015_Operadores_de_Comparacao {

    
    public static void main(String[] args) {
        int x = 6;
        Integer y = 6;
        System.out.println(x == 7);
        System.out.println(x != 7);
        System.out.println(x > 7);
        System.out.println(x < 7);
        System.out.println(x >= 7);
        System.out.println(x <= 7);
        
        System.out.println(y instanceof Integer);
        System.out.println("Oi" instanceof String);
        
        
    }
    
}
