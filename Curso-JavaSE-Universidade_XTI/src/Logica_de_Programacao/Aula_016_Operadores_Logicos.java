public class Aula_016_Operadores_Logicos {

    
    public static void main(String[] args) {
        int x = 6;
        System.out.println((x >= 7) && (x <= 10));
        System.out.println((x >= 7) || (x <= 10));
        System.out.println(!(x >= 7));
        System.out.println(x >= 7);
    }
    
}
