package Aula_073_Math;

public class MathTest {

	public static void main(String[] args) {
		System.out.println(Math.sin(10)); // Seno
		System.out.println(Math.cos(10)); // Cosseno
		System.out.println(Math.tan(45)); // Tangente
		System.out.println(Math.pow(4, 3)); // Potência
		System.out.println(Math.sqrt(16)); // Raiz quadrada
		System.out.println(Math.cbrt(16)); // Raiz cúbica
		System.out.println(Math.hypot(6, 8)); // Hypotenusa

	}

}
